package variable;

import java.lang.*;

public class Test01 {
	public static void main(String[] args) {
		//정수 : 소수점이 없는 숫자
		int a = 10;
		int b = 100 * 200;
		System.out.println(a);
		System.out.println(b);
		
		int d = 10 / 3; // 몫
		System.out.println(d);
		
		int e = 10 % 3; // 나머지
		System.out.println(e);
		
		long f = 10;
		System.out.println(f);
		
	}
}
